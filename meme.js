  function genText() {
    document.getElementById('test2').innerHTML = ""
    var input = document.getElementById("phrase").value;
    var emoji = document.getElementById("emoji").value;
    var spaceremoji = document.getElementById("space").value;
    var inputarray = input.toUpperCase().split('');
    var outarray = ["","","","",""]
    for (var i = 0; i < inputarray.length; i++) {
      for (var j = 0; j < 5; j++) {
        for (var k = 0; k < alpha[inputarray[i]][0].length; k++) {
          if (alpha[inputarray[i]][j][k] == 1){
            outarray[j] += emoji;
          }
          else outarray[j] += spaceremoji;
          if (k == alpha[inputarray[i]][0].length - 1 && i != inputarray.length - 1) {
            outarray[j] += spaceremoji;
          }
        }
      }
    }
    for (var q = 0; q < 5; q++) {
      document.getElementById('test2').innerHTML += outarray[q] + "\n";
    }
  }

  function copyToClipboard() {
    const el = document.createElement('textarea');
    el.value = document.getElementById('test2').value;
    document.body.appendChild(el);
    el.select();
    document.execCommand('copy');
    document.body.removeChild(el);
  }